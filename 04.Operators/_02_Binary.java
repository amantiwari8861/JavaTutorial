public class _02_Binary {

    public static void main(String[] args) {
      
        // 2. Binary 
        //      1.Arithmetic Operator eg +,-,*,/,%

        int num1=10,num2=3,result;

        result=num1+num2;
        System.out.println(num1+" + "+num2+" = "+result);
        result=num1-num2;
        System.out.println(num1+" - "+num2+" = "+result);
        result=num1*num2;
        System.out.println(num1+" * "+num2+" = "+result);
        result=num1/num2;
        System.out.println(num1+" / "+num2+" = "+result);

    }
}